    private void AddButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddButtonActionPerformed
        // TODO add your handling code here:
        String empName = EmpNameTF.getText();
        String empEmail = EmpEmailTF.getText();
        String empphoneNo = EmpNoTF.getText();
        String empPwd = EmpPwd.getText();
        String empJoinDate = ((JTextField) jDateChooser1.getDateEditor().getUiComponent()).getText();
        int empSalary = Integer.parseInt(EmpSalaryTF.getText());
        
        if (EmpNameTF.getText().equals("") || EmpEmailTF.getText().equals("") || EmpNoTF.getText().equals("") || EmpPwd.getText().equals("") || EmpSalaryTF.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Provide Complete Information", "", JOptionPane.ERROR_MESSAGE);
        } else {
            try {
                Connection con = MySqlConnection.getCon();
                
                String query = "ALTER TABLE addemployee AUTO_INCREMENT=1;";
                PreparedStatement ps = con.prepareStatement(query);
                ps.execute();
                
                if (empphoneNo.length() == 11 && empPwd.length() >= 8) {
                    query = "INSERT INTO addemployee VALUES(?,?,?,?,?,?,?)";
                    ps = con.prepareStatement(query);
                    ps.setString(1, null);
                    ps.setString(2, empName);
                    ps.setString(3, empEmail);
                    ps.setString(4, empphoneNo);
                    ps.setString(5, empPwd);
                    ps.setString(6, empJoinDate);
                    ps.setInt(7, empSalary);
                    ps.executeUpdate();
                    
                    new AddEmployee().setVisible(true);
                    JOptionPane.showMessageDialog(null, "Employee Added Successfully!");
                } else {
                    JOptionPane.showMessageDialog(null, "Phone No. & Password length should be 11 and 8 respectively!", "", JOptionPane.ERROR_MESSAGE);
                }
                
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e, "Error:", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_AddButtonActionPerformed
    
    public void fillComboBox2() {
        String getSelectedName = jComboBox1.getSelectedItem().toString();
        try {
            Connection con = MySqlConnection.getCon();
            String query = "select * from addemployee WHERE emp_name='" + getSelectedName + "'";
            PreparedStatement ps = con.prepareStatement(query);
            ResultSet rs = ps.executeQuery();
            while (rs.next()) {
                jComboBox1.addItem(rs.getString("emp_name"));
            }
            con.close();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e, "Error", JOptionPane.ERROR_MESSAGE);
        }
    }
    
