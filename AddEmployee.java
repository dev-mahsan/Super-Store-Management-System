    private void jComboBox1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBox1ActionPerformed
        // TODO add your handling code here:
        String emp_name = jComboBox1.getSelectedItem().toString();
        String emp_phoneNo = "";
        String password = "";
        String joinDate = "";
        int Salary = 0;
        String emp_email = "";
        
        try {
            Connection con = MySqlConnection.getCon();
            String query = "Select * from addemployee WHERE emp_name='" + emp_name + "'";
            PreparedStatement ps = con.prepareStatement(query);
            ResultSet rs = ps.executeQuery(query);
            while (rs.next()) {
                emp_email = rs.getString(3);
                emp_phoneNo = rs.getString(4);
                password = rs.getString(5);
                joinDate = rs.getString(6);
                Salary = rs.getInt(7);
            }
            
            System.out.println(joinDate);
            EmpNameTF.setText(jComboBox1.getSelectedItem().toString());
            EmpEmailTF.setText(emp_email);
            EmpNoTF.setText(emp_phoneNo);
            EmpPwd.setText(password);
//            jDateChooser1.setDate(joinDate);
            java.util.Date date2 = new SimpleDateFormat("yyyy-MM-dd").parse(joinDate);
            jDateChooser1.setDate(date2);
            EmpSalaryTF.setText(String.valueOf(Salary));
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e, "Error", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_jComboBox1ActionPerformed

    private void RemoveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_RemoveButtonActionPerformed
        // TODO add your handling code here:
        String removeEmployee = jComboBox1.getSelectedItem().toString();
        if (jComboBox1.getSelectedItem().equals("Select")) {
            JOptionPane.showMessageDialog(null, "Please select the employee to remove", "", JOptionPane.ERROR_MESSAGE);
        } else {
            try {
                Connection con = MySqlConnection.getCon();
                String query = "DELETE FROM addemployee WHERE emp_name='" + removeEmployee + "'";
                PreparedStatement ps = con.prepareStatement(query);
                ps.execute();
                
                query = "update addemployee ae\n"
                        + "JOIN\n"
                        + "(\n"
                        + "SELECT ROW_NUMBER() OVER (\n"
                        + "        ORDER BY emp_id\n"
                        + "    ) row_num, emp_id\n"
                        + "    FROM addemployee\n"
                        + ")AS rw_num \n"
                        + "ON ae.emp_id = rw_num.emp_id\n"
                        + "set ae.emp_id = rw_num.row_num";
                ps = con.prepareStatement(query);
                ps.execute();
                
                JOptionPane.showMessageDialog(null, "Employee has been removed successfully!");
                EmpNameTF.setText("");
                EmpEmailTF.setText("");
                EmpNoTF.setText("");
                EmpPwd.setText("");
                EmpSalaryTF.setText("");
                jDateChooser1.setDate(null);
                new AddEmployee().setVisible(true);
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e, "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_RemoveButtonActionPerformed

