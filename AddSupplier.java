    private void RemoveSupplierActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_RemoveSupplierActionPerformed
        // TODO add your handling code here:
        String selectedSupplier = SupplierComboBox.getSelectedItem().toString();
//        SupplierTextField.setText(selectedSupplier);
        if (SupplierComboBox.getSelectedItem().toString().equals("Select")) {
            JOptionPane.showMessageDialog(null, "Select Supplier to Remove!", "", JOptionPane.ERROR_MESSAGE);
        } else {
            try {
                Connection con = MySqlConnection.getCon();
                String query = "DELETE from supplier WHERE supplier_name='" + selectedSupplier + "';";
                PreparedStatement ps = con.prepareStatement(query);
                ps.execute();
                
                query = "update supplier supp\n"
                        + "JOIN\n"
                        + "(\n"
                        + "SELECT ROW_NUMBER() OVER (\n"
                        + "        ORDER BY supplier_id\n"
                        + "    ) row_num, supplier_id\n"
                        + "    FROM supplier\n"
                        + ")AS rw_num \n"
                        + "ON supp.supplier_id = rw_num.supplier_id\n"
                        + "set supp.supplier_id = rw_num.row_num";
                ps = con.prepareStatement(query);
                ps.execute();
                SupplierTextField.setText("");
                JOptionPane.showMessageDialog(null, "Supplier Removed Successfully", "", JOptionPane.ERROR_MESSAGE);
//                SupplierComboBox.setSelectedItem(null);
                AddSupplier as = new AddSupplier();
                as.setVisible(true);
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e, "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_RemoveSupplierActionPerformed

