    private void AddSupplierActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddSupplierActionPerformed
        // TODO add your handling code here:
        String SupplierName = SupplierTextField.getText();
        if (SupplierTextField.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Supplier Name is not entered!", "", JOptionPane.ERROR_MESSAGE);
        } else {
            try {
                Connection con = MySqlConnection.getCon();
                String query = "ALTER TABLE supplier AUTO_INCREMENT=1;";
                PreparedStatement ps = con.prepareStatement(query);
                ps.execute();
                
                query = "insert into supplier values (?,?)";
                ps = con.prepareStatement(query);
                ps.setString(1, null);
                ps.setString(2, SupplierName);
                ps.executeUpdate();
                JOptionPane.showMessageDialog(null, "Supplier Added Successfully");
                fillComboBox2();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e, "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_AddSupplierActionPerformed

